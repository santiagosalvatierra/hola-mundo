package holamundo;
public class Toro {
  private String nombre;
  private String raza;
  private Integer peso;

    public Toro() {
    }

    public Toro(String nombre, String raza, Integer peso) {
        this.nombre = nombre;
        this.raza = raza;
        this.peso = peso;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getRaza() {
        return raza;
    }

    public void setRaza(String raza) {
        this.raza = raza;
    }

    public Integer getPeso() {
        return peso;
    }

    public void setPeso(Integer peso) {
        this.peso = peso;
    }
  
  
    
    
    
    
    
}
